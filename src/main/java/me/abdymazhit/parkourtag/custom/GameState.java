package me.abdymazhit.parkourtag.custom;

/**
 * Represents a game state
 */
public enum GameState {
    /**
     * Represents the waiting state for the game to start
     */
    WAITING,
    /**
     * Represents the start state of the game
     */
    STARTING,
    /**
     * Represents a game progression state
     */
    GAME,
    /**
     * Represents the end game state
     */
    ENDING
}
